<h2>🚨 Solución de Problemas en Reportes</h2>

<div class="step-card">
    <div class="step-content">
        <h5><i class="fas fa-tools text-primary"></i> Problemas Comunes</h5>
        
        <div class="row">
            <div class="col-md-6">
                <h6>❌ "Los datos no coinciden"</h6>
                <p><strong>Verificar:</strong></p>
                <ul>
                    <li>Rango de fechas seleccionado</li>
                    <li>Filtros de tienda aplicados</li>
                    <li>Sincronización de datos completada</li>
                    <li>Permisos de acceso a todas las tiendas</li>
                    <li>Cambios recientes en configuración</li>
                </ul>
                
                <h6>🐌 "Los reportes cargan lento"</h6>
                <p><strong>Optimizar:</strong></p>
                <ul>
                    <li>Reducir el rango de fechas</li>
                    <li>Limitar el número de tiendas</li>
                    <li>Usar filtros más específicos</li>
                    <li>Evitar múltiples reportes simultáneos</li>
                </ul>
            </div>
            <div class="col-md-6">
                <h6>📊 "Gráficos no se muestran"</h6>
                <p><strong>Verificar:</strong></p>
                <ul>
                    <li>Navegador compatible (Chrome, Firefox)</li>
                    <li>JavaScript habilitado</li>
                    <li>Bloqueador de anuncios desactivado</li>
                    <li>Conexión de internet estable</li>
                </ul>
                
                <h6>📧 "No llegan reportes automáticos"</h6>
                <p><strong>Revisar:</strong></p>
                <ul>
                    <li>Carpeta de spam/correo no deseado</li>
                    <li>Dirección de email correcta</li>
                    <li>Configuración de servidor de email</li>
                    <li>Programación de horarios activa</li>
                </ul>
            </div>
        </div>
    </div>
</div>

